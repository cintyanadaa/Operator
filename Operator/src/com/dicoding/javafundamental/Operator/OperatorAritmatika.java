package com.dicoding.javafundamental.Operator;

/**
 *
 * @author Sindy
 */
public class OperatorAritmatika {
    public static void main(String[] args){
        System.out.println("operasi penjumlahan");
        int hasilpenjumlahan = 10+5;
        System.out.println("hasil 10+5 = " + hasilpenjumlahan);
        System.out.println();

        System.out.println("operasi pengurangan");
        int hasilpengurangan = 20-10;
        System.out.println("hasil 20-10 = " + hasilpengurangan);
        System.out.println();

        System.out.println("operasi perkalian");
        int hasilperkalian = 5*5;
        System.out.println("hasil 5*5 = " + hasilperkalian);
        System.out.println();

        System.out.println("operasi pembagian");
        int hasilpembagian = 20/2;
        System.out.println("hasil 20/2 = " + hasilpembagian);
        System.out.println();

        System.out.println("operasi habis bagi");
        int hasilsisa = 9%3;
        System.out.println("hasil 9%3 = " + hasilsisa);
        int hasilsisalain = 9%2;
        System.out.println("hasil 9%2 = " + hasilsisalain);
        System.out.println();
    }
    
}
